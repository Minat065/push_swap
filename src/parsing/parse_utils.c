#include "../../includes/push_swap.h"

int	check_duplicates(int *arr, int size)
{
	int	i;
	int	j;

	i = 0;
	while (i < size - 1)
	{
		j = i + 1;
		while (j < size)
		{
			if (arr[i] == arr[j])
				return (1);
			j++;
		}
		i++;
	}
	return (0);
}

long	ft_atol(const char *str)
{
	long	result;
	int		sign;
	int		i;

	result = 0;
	sign = 1;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	return (result * sign);
}

int	count_split_size(char **split)
{
	int	size;

	size = 0;
	while (split[size])
		size++;
	return (size);
}

int	*convert_split_to_int_array(char **split, int size)
{
	int	*result;
	int	i;

	result = malloc(sizeof(int) * size);
	if (!result)
		return (NULL);
	i = -1;
	while (++i < size)
	{
		if (!validate_number(split[i]))
		{
			free(result);
			return (NULL);
		}
		result[i] = (int)ft_atol(split[i]);
	}
	return (result);
}
