#include "../../includes/push_swap.h"

void	push_swap(t_stack *a, t_stack *b)
{
	if (!a || !b)
		return ;
	if (is_sorted(a))
		return ;
	if (a->size == 2)
		sort_two(a);
	else if (a->size == 3)
		sort_three(a);
	else if (a->size == 4)
		sort_four(a, b);
	else if (a->size == 5)
		sort_five(a, b);
	else if (a->size <= 100)
		turkish_sort(a, b);
	else
		turkish_sort_optimized(a, b);
}
